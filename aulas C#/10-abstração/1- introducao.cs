/*
A abstração é um dos quatro pilares fundamentais da programação 
orientada a objetos (POO), juntamente com a encapsulação, 
herança e polimorfismo.

A abstração é o processo de ocultar os detalhes internos e 
exibir apenas a funcionalidade ao usuário. Em outras palavras, 
é uma maneira de ocultar a implementação complexa e mostrar ao 
usuário o que ele precisa saber. Isso pode ser conseguido 
usando classes abstratas e interfaces.

Classe abstrata: Uma classe abstrata é uma classe que
não pode ser instanciada e é sempre usada como uma 
classe base. Uma classe abstrata pode ter métodos 
abstratos (métodos sem corpo) bem como métodos normais.

Interface: Uma interface em C# é um tipo que define 
um conjunto de funções. Essa interface não fornece 
qualquer implementação para essas funções; ela apenas 
 sua existência. É tarefa das classes que implementam 
 a interface fornecer a implementação para essas funções.

A abstração permite que o código seja reutilizado e 
reduz a complexidade. Ela permite que os desenvolvedores 
criem camadas em suas aplicações, ocultando o código 
complexo por meio do uso de interfaces e classes abstratas. 
Isso torna o código mais gerenciável e mais fácil de atualizar ou alterar.

*/