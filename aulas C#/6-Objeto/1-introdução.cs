//Introdução ao conceito de objetos

/*
O que é um objeto?
Um objeto é uma instância de uma classe que representa uma 
entidade do mundo real. Ele é composto por propriedades 
(também chamadas de atributos)e métodos (também chamados de funções).
Objetos são as unidades básicas de um programa orientado a objetos e 
servem como blocos de construção para criar sistemas complexos.
Eles encapsulam dados e comportamentos relacionados, 
permitindo abstrair e modelar conceitos do 
mundo real de forma mais fácil e organizada.
*/

/*
O que é uma classe?
Uma classe é um modelo ou um esquema usado para criar objetos.
Ela define a estrutura e o comportamento de um objeto, especificando
suas propriedades e métodos.Classes são como plantas que descrevem o 
formato, os atributos e as ações de um objeto. Elas fornecem um meio 
de organizar e reutilizar código, facilitando a manutenção e a 
escalabilidade do software.


Por que usar objetos e classes?
A utilização de objetos e classes tem várias vantagens no 
desenvolvimento de software, especialmente em linguagens 
orientadas a objetos como o C#. Algumas dessas vantagens incluem:

Modularidade: Objetos e classes permitem dividir um sistema
complexo em partes menores e mais gerenciáveis,
facilitando o desenvolvimento,
a manutenção e o entendimento do código.

Reutilização de código: Classes podem ser reutilizadas
em diferentes partes do programa 
ou até mesmo em outros projetos, reduzindo a duplicação 
de código e melhorando a produtividade.

Encapsulamento: Objetos encapsulam dados e comportamentos
relacionados, separando a implementação interna dos detalhes 
de como eles são expostos e acessados por outros objetos.
Isso promove o isolamento de responsabilidades e facilita 
a evolução do código.

Abstração: Classes permitem modelar conceitos do mundo 
real de forma mais clara e natural, facilitando o 
entendimento e a comunicação entre desenvolvedores e partes interessadas.
Polimorfismo: Objetos podem ter diferentes formas e comportamentos, 
facilitando a criação de sistemas flexíveis e extensíveis.

Herança: Classes podem herdar propriedades e métodos
de outras classes, promovendo a reutilização de código 
e reduzindo a complexidade do sistema.
*/